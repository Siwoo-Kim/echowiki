== [[:분류:에코위키의 기능|기능]] ==
[include(틀:상세 내용, 문서명=분류:에코위키의 기능)]
에코위키는 자체 [[위키 엔진]]인 [[the seed]]과 자체적인 텍스트 문법인 [[나무마크]][* [[리그베다 위키]]에서 사용하는 [[모니위키]] 문법과 100% 호환이 되지 않는다. 때문에 리그베다 위키에서 [[포크]]한 문서들의 초기 판본을 보면 [[하이퍼링크]]나 이미지 파일이 깨져있는 경우가 많다. 이로 인해 설립 초창기에 [[namubot]]이라는 [[봇#s-2]]을 돌려 호환되지 않는 모니위키 문법을 수정했다.]를 사용한다. [[HTTP/2]]를 지원하며, 아래에 있는 주요 기능 목록의 하이퍼링크를 누르면 각 기능을 설명하는 문서로 연결된다.

 * [[the seed]] - 에코위키의 [[위키 엔진]].
 * [[the seed forum]] - 위키 엔진인 the seed engine에 관하여 기술적 지원 문의를 받는 곳이다. [[https://forum.theseed.io/|바로가기]]
 * [[나무마크]] -  에코위키의 텍스트 문법.
 * [[에코위키:도움말|도움말]] - 에코위키에 처음 온 사용자들을 위한 도움말 문서다.
 * [[에코위키:FAQ|FAQ]] - 에코위키로부터 자주 들어오는 질문에 대한 답변을 하는 문서다.
 * [[에코위키:가이드]] - 에코위키의 주요 기능 및 단축키를 설명하는 문서.
 * [[문서#s-3]] - 에코위키에 존재하는 개별 페이지들을 문서라고 한다. [[:분류:에코위키의 규정|에코위키의 규정]]에 맞게 문서를 작성해야 하며, 나무마크를 통해 문서를 편집할 때 다양한 기능을 사용할 수 있다.
 * [[에코위키:가이드/이미지 업로드|이미지 업로드]] - 파일을 에코위키 서버에 올려서 문서에서 활용할 수 있게 해 주는 기능이다. [[:분류:파일]] 문서에서 에코위키에 업로드 된 파일 목록을 확인할 수 있다.[* 이전에는 [[imgur]] 등의 외부 사이트에 이미지를 올리고 에코위키에 핫링크를 했었지만 지금은 기능을 막아두었다. 자세한 이유는 [[imgur#s-2.1|여기]]를 참고.]
 * [[분류#s-1.1|분류]] - 에코위키 문서들을 각 주제에 맞게 분류할 수 있는 기능이다. 최상위 분류는 [[:분류:분류]] 문서.
 * [[틀#s-2|틀]] - 안내 기능을 하거나 비슷한 다른 문서들을 묶어주는 박스이다. [[에코위키:틀]] 문서나 [[:분류:틀]] 문서에서 에코위키의 틀 목록을 확인할 수 있다.
 * [[:분류:템플릿|템플릿]] - 동일한 주제의 여러 문서들에 일괄적으로 적용되는 문서를 만들 때 사용하는 양식이다. [[:분류:템플릿]] 문서에서 템플릿 목록을 확인할 수 있다.
 * [[에코위키:토론 도움말|토론]] - 문서 작성에 관해 의견 충돌이 벌어졌을 경우, 토론 합의를 통해 더 좋은 문서를 만들어가기 위한 기능이다.
 * [[에코위키:내 문서함|내 문서함(스타)]] - 주시 문서 기능으로, 로그인 후 내 문서함에서 스타를 체크한 문서 목록을 문서의 최근 편집일자 순으로 모아서 볼 수 있다.
 * [[에코위키:프로젝트]] - 에코위키 사용자들이 위키 기여를 더욱 알차게 하기 위해 자발적으로 개설하여 서로 협력하면서 문서 기여를 하는 프로젝트이다. 프로젝트 목록은 [[:분류:에코위키 프로젝트]] 문서에서 볼 수 있다.
 * [[에코위키:문서 작성 요청]] - 에코위키에 아직 없는 문서 중 필요하다고 느껴지는 문서들을 요청하는 집단연구 성격의 메타 문서이다.
 * [[에코위키:주요 페이지 링크]] - 리그베다 위키 시절의 흔적으로, 각 주제별로 문서를 모아놓는 [[메타 문서]] 목록의 일부를 나열한 문서이다. 메타 문서 목록은 [[:분류:메타 문서]]에서 확인할 수 있다.
 * [[에코위키:게시판|게시판]] - 에코위키의 공지사항이 올라오는 곳이며, 이곳에서 문의, 신고, 소명 등 여러가지 업무를 처리한다. 그루터기에서 사용자들이 에코위키와 관련된 글을 올릴 수 있다.
 * [[에코위키:통계|통계]] - 에코위키의 문서 개수를 확인할 수 있는 곳이다.
 * [[에코위키:연습장|연습장]] - 에코위키의 여러가지 위키 문법과 편집 기술들을 연습할 수 있는 곳이다.
 * [[에코위키:보존문서|보존문서]] - 기록 보존을 위해 보존된 운영 관련 문서들이다. [[:분류:보존문서]] 문서에서 보존문서의 목록을 확인할 수 있다.
