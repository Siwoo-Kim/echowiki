 * 에코위키에는 모든 이용자가 지켜야 하는 규정이 있습니다. 여러 사람들이 이용하는 공간이기 때문에 규칙이 있고, 에코위키에서는 이를 ''''''규정''''''이라고 부릅니다.
  * 규정은 크게 ''''''방침''''''과 ''''''지침''''''으로 나뉩니다. 방침은 ''''''운영/토론/이용자 관리''''''에 대한 규정을 담고 있고, 지침은 ''''''문서 편집''''''에 대한 규정을 담고 있습니다. 우리나라 법의 헌법에 대응하는 에코위키 최상위 규정은 ''''''[[에코위키:기본방침|기본 방침]]''''''이며 모든 규정은 기본 방침에 위배되어서는 안 됩니다.

=== 운영진 ===
 * 에코위키는 파라과이 소재의 법인 ''''''[[umanle S.R.L.]]''''''이 운영하고 있습니다. 이들은 ''''''사측(社側)''''''으로도 불립니다. 에코위키는 ''''''사측''''''과 사측에 의하여 선출되는 선출직 운영자들의 도움으로 운영되고 있습니다.
 * ''''''[[umanle S.R.L.]]'''''', 즉 사측에 소속된 직원들을 ''''''사측 관리자''''''라고 부릅니다. 현재 에코위키를 운영하는 사측 관리자 계정은 두 종류로, ''''''[[사용자:관리자|관리자]]''''''와 ''''''[[사용자:umanle|umanle]]''''''라는 계정명을 사용하고 있습니다.[* 이들의 계정명과 매우 유사하거나, 사측 관리자로 오인할수 있을만한 다른 계정명을 사용하는 이용자를 발견하신 경우 즉시 [[https://board.namu.wiki/report|신고게시판]]을 통하여 신고하여 주시기 바랍니다. 이는 부적절한 계정명에 해당합니다.]
 * 에코위키는 매우 규모가 큰 위키 사이트기 때문에 사측의 인력 만으로는 모든 업무를 처리하지 못합니다. 그래서 사측은 일반 이용자들 중에서 지원을 받아 운영자를 선출하여 운영에 도움을 받습니다. 이 운영자들을 ''''''선출직 운영자''''''라고 부릅니다.
  * 선출직 운영자에는 ''''''[[에코위키:관리자|관리자]]''''''와 ''''''[[에코위키:중재자|중재자]]''''''가 있습니다. 관리자는 에코위키의 업무 전반을 처리하는 운영자를 말하고, 중재자는 토론의 중재와 관련한 업무를 처리하는 운영자를 말합니다.
  * 회원가입한 이용자들은 누구나 운영자에 지원할 수 있습니다. 다만 최근에 가입했거나, 관리자의 경우 기여내역이 300회 이내, 중재자의 경우 토론 기여내역이 낮은 사용자는 선출되지 못할 가능성이 매우 높습니다. 지원을 희망하는 사용자는 [[https://namu.wiki/discuss/%EB%82%98%EB%AC%B4%EC%9C%84%ED%82%A4%3A%EC%9A%B4%EC%98%81%EC%A7%84%20%EC%A7%80%EC%9B%90|이곳]]에서 지원하실 수 있습니다.

== 토론 ==
자세한 내용은 ''''''[[에코위키:토론 도움말|토론 도움말]]''''''과 ''''''[[에코위키:기본방침/토론 관리 방침|토론 관리 방침]]''''''을 참조하세요.
 * 문서를 편집하다 보면 분쟁이 생길 수 있습니다. 에코위키에서는 이런 경우 토론으로 해결해야 합니다. 토론하기 전에 토론에 대한 기본예절과 절차는 [[에코위키:토론 도움말]]을 참조하세요.
 * 토론에서도 위키 문법이 적용됩니다. (단, 일부 문법은 적용되지 않습니다) 토론에서의 위키 문법을 연습하시려면 [[에코위키:연습장]]의 [[https://namu.wiki/discuss/%EB%82%98%EB%AC%B4%EC%9C%84%ED%82%A4:%EC%97%B0%EC%8A%B5%EC%9E%A5|토론]]에 연습용 토론이 열려 있으니 그곳을 이용해 주세요.
 * 간단한 가이드:[[에코위키:토론 도움말]]이 읽기 버거우시다면 이것이라도 읽고 토론에 임하세요.
  * 상대의 발언에 감정적으로 대응하지 마세요! 에코위키에서 토론으로 차단당하는 대부분은 감정적인 대응 때문인 경우가 많습니다.
  * 상대가 근거를 제시하라고 요구하면 제시해야 합니다! 근거 없는 주장을 반복하면 차단의 사유가 됩니다.
  * 편집 분쟁 중이라고 상대의 아이디를 토론 제목에 언급해서는 안 됩니다! 차단의 사유가 됩니다. 호출은 사용자 토론을 이용하세요.
  * 충분히 근거를 제시하였음에도, 토론이 원활하게 진행이 안될 때, [[https://board.namu.wiki/discuss|토론 문의 게시판]]에서 중재 요청을 하시면 담당 중재자께서 도움을 주실겁니다.


