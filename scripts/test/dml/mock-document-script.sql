== 개설자 ==
에코위키의 개설자는 ''''''[[namu]],'''''' [[에코위키:전직 특수 운영진#s-1.1.5|PPPP]], [[kasio]]이다. 이들은 [[리그베다 위키 게시판]]에 [[유동닉]]으로 처음 등장했는데, 에코위키 개설자들은 특정 단체나 집단의 이익을 위해 행동하고 있지 않으며, 에코위키 개설자들은 지식의 사유화를 반대하며 평화로운 방식으로 지식의 자유로운 공유를 지원하는 것이 목표라고 밝혔다. 에코위키 설립 이후 [[ztirf]], [[syndrome]]이 합류하면서 초창기 운영진이 구성되었다.[* namu가 직접 이 문서를 편집하면서 [[https://namu.wiki/history/에코위키?from=3249|수정코멘트]]로 ztirf와 syndrome은 에코위키가 설립된 이후에 합류하였다고 밝혔다.] [[:파일:namuwiki_n4.jpg|2015년 5월 8일 공지사항]]에서 syndrome이 밝힌 당시 운영진들의 소임은 다음과 같다.

 * namu - 백엔드 개발자
 * PPPP - 프론트엔드 개발자
 * kasio - 렌더러 개발자
 * ztirf - 위키 관리 및 게시판 관리
 * syndrome - 이슈 트래커 및 기타 이슈 관리

에코위키 개설자들은 신상을 비공개하고 있는데, 여러 이유가 존재하지만 주된 이유는 법적 분쟁을 의식하고 있기 때문이다. 일단 개발자들의 신원이 밝혀지면 에코위키에 불만이 있는 개인 혹은 단체들이 사방에서 고소를 걸 가능성이 매우 높은데다[* [[청동(인물)|청동]]이 [[리그베다 위키]]를 운영할 때 문서 관련해서 [[고소(법률)|고소]]·[[고발]]에 상당히 자주 시달렸다.], 리그베다 위키 측에서 [[법무법인]]을 동원한 광범위한 고소를 할 가능성도 배제할 수 없기 때문에 익명을 유지하고 있다. 개설자 namu의 [[:파일:namuwiki_n5.jpg|에코위키 Q&A 게시판 답변]]에 따르면 본업이 [[정보보호|정보보안]]과 관련되어 있다고 한다.

에코위키가 소유권 이전을 거쳐 [[파라과이]] 법인 유한책임회사 [[umanle S.R.L.]]에 인수된 이후로는 에코위키 개설자들은 별도의 특별한 권한이 없다.

