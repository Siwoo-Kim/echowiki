=== [[에코위키:문서 삭제식 이동|문서 삭제식 이동]] ===
문서 제목을 변경하기 위해 문서를 삭제하고 다른 문서로 내용을 복사 - 붙여넣기 하는 것을 말합니다. 이렇게 할 경우 이 문서의 원래 기여자가 누구였는지 알 수 없게 되고, 다른 사람들이 보기에는 마치 이동한 사용자의 단독 기여로 만들어진 문서처럼 보이게 됩니다. 그렇기 때문에 이런 행동은 저작권 침해 행위로 간주될 수 있습니다. 자세한 내용은 [[에코위키:문서 삭제식 이동|문서 삭제식 이동]]을 참조하세요.

문서의 제목을 변경하시려면 편집 탭에 들어가서 이동을 누르시고 변경할 제목을 입력해주세요. 권한이 없다면 문의 게시판에 이동을 요청하시면 됩니다.

