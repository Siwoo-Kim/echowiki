=== [[수정 코멘트]] 및 [[토론]]에서의 말실수 ===
[[에코위키:기본방침/이용자 관리 방침]]에 따라 비하적, 모욕적 표현을 사용하는 등 잘못된 수정 코멘트를 적는 경우는 편집 요약 불량에 해당합니다. 수정 코멘트는 가급적 편집의 목적이나 내용을 요약하는 용도로 사용해주세요. 또한 토론에서 비하, 모욕적 표현 및 반말을 사용하시는 것은 이용자 관리 방침에서 정하는 [[에코위키:기본방침/이용자 관리 방침#s-4|토론 관련 규정]] 위반입니다. 주의해주세요.

=== 요청형, 질문형, 통보형 토론 발제 ===
에코위키의 토론은 문서의 편집 방향에 이견이 발생했을 때 이를 조율하고 편집 방향을 합의하는 목적으로만 활용되고 있습니다. 즉 [[토의]]가 아닌 [[토론]]만 가능합니다. 단순히 편집 요청을 하시려면 [[편집 요청]] 기능을 이용해주십시오. 더불어, 본문 편집과 무관하게 단순히 궁금증이 생겨서 질문하거나, 혹은 자신의 편집 사실을 알리기 위한 통보를 하는 것 역시 적절한 토론 개설방법이 아닙니다. 자세한 내용은 [[에코위키:토론 도움말]] 또는 [[토론 관리 방침]]을 참조하십시오.[* 후자는 에코위키 공식 규정이고, 전자는 공식 규정을 초보자를 위해 쉽게 풀어쓴 설명입니다. 초보라면 전자를 우선 읽는 것을 추천합니다.]
다만 프로젝트 문서의 토론에서는 프로젝트가 관할하는 문서들에 대한 질문성 또는 요청성 토론의 발제가 가능합니다. 서로의 의견을 나누고 협업하여 문서를 기여해 보세요!
=== 차단 소명 관련 실수 ===
 * 차단을 당하신 후 소명을 하실 때 이용자께서 당황하신 나머지 차단당한 아이피나 아이디를 아예 적지 않으시는 경우도 있습니다. 꼭 아이피나 아이디를 적어주셔야 소명 처리를 해드릴 수 있습니다.
 * 차단을 당하셨을 때에는 차단 소명 게시판만 이용할 수 있습니다. 실수로 문의 게시판에 쓰셨을 때 차단 회피로 처리되지 않습니다. 운영자에게 차단 소명 게시판으로 안내를 받으실 수 있지만 웬만하면 차단 소명 게시판을 바로 이용해주시기 바랍니다. 문의 게시판에서는차단 소명 게시판에서의 글 작성이 불가능하다거나 하는 경우를 제외하면 차단 소명 처리를 하지 않기 때문입니다. 또한 문의 게시판과 차단 소명 게시판 외 다른 게시판에 글을 작성하시면 차단 회피 처리를 받으니 주의해주세요.
 * 차단된 상태에서 부계정이나 다른 아이피로 접속해서 문서 편집이나 토론을 계속할 경우 [[차단 회피]]로 징계기간이 길게 연장될 수 있습니다.
 * 차단 소명 글을 작성하실 때는 절대 타 이용자를 제재해달라는 글을 작성하시면 안됩니다. 이 경우 소명글은 즉시 각하되며, 경우에 따라 차단 기간이 연장 될 수도 있습니다. 타 이용자 제재 요청은 차단 해제 이후에 신고 게시판에서 해주세요.

